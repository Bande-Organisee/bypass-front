    <h2>Les outils pour sécuriser les données</h2>
    Doter son système d’un antivirus est important mais ne suffit plus.
    Associez-le à un système anti-APT (Advanced Persistent Threat) qui vous
    protégera contre les attaques les plus avancées en offrant une détection
    approfondie. Sur Internet, mettez en place une solution Firewall qui protège
    le point d’accès Internet de votre entreprise. Elle permet de filtrer les
    données, de lutter contre les logiciels malveillants, les spams et la fuite
    de données. Contrôlez également les accès Internet de votre entreprise en
    privilégiant le réseau sécurisé privé virtuel (VPN). Vos réseaux d’accès
    WiFi doivent également être sous contrôle, car bien souvent, les bornes WiFi
    sont accessibles depuis l’extérieur des locaux. Enfin, pour protéger vos
    données et préserver votre activité, pensez aux serveurs de sauvegarde
    sécurisés. Ils vous permettront de restaurer vos données en cas d’attaque.
    De nombreuses solutions existent, notamment si vous travaillez en Cloud.
